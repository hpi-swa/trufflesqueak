        if (bits == null) {
            return;
        }
        final int bmWidth = bitmapWidthOf(bmFill);
        final int bmHeight = bitmapHeightOf(bmFill);
        final boolean tileFlag = bitmapTileFlagOf(bmFill) == 1;
        final int deltaX = leftX - fillOriginXOf(bmFill);

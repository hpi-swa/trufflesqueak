        final int nLines;
        final int startY;
        int xDir;
        final int yEntry;
        final int yExit;

        /* Get some values */
        lineWidth = wideBezierExtentOf(bezier);
        /* Compute the incremental values of the bezier */
        lineOffset = offsetFromWidth(lineWidth);
        endX = bezierEndXOf(bezier);
